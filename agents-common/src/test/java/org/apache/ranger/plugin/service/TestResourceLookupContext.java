@ExtendWith(MockitoExtension.class)
@TestMethodOrder(MethodOrderer.MethodName.class)
public class TestResourceLookupContext {
    @Test
    public void test1_GettersSettersAndToString() {
        ResourceLookupContext ctx = new ResourceLookupContext();
        ctx.setUserInput("abc");
        ctx.setResourceName("table");

        Map<String, List<String>> resources = new HashMap<>();
        List<String> vals = new ArrayList<>();
        vals.add("v1");
        resources.put("key", vals);
        ctx.setResources(resources);

        assertEquals("abc", ctx.getUserInput());
        assertEquals("table", ctx.getResourceName());
        assertEquals(1, ctx.getResources().get("key").size());

        String s = ctx.toString();
        assertTrue(s.contains("resourceName=table"));
        assertTrue(s.contains("userInput=abc"));
        assertTrue(s.contains("resources={key=[v1]"));
    }
}
